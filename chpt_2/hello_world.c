/* Header includes C's standard input and output library -- stdio.h */
#include <stdio.h>

/* declare a function 'main' that will output an int and takes as arguments an int and a char** */
/* all C programs must include a main function -- all programs start running from this function */
int main(int argc, char** argv) {
  puts("Hello World"); /* the 'puts' function is from the stdio.h library */
  return 0; /* tells the main function to finish and return 0 */
}
